// Function to get a verse by reference
function getVerse(book, chapter, verse) {
    if (KJV_DATA[book] && KJV_DATA[book][chapter] && KJV_DATA[book][chapter][verse]) {
        return KJV_DATA[book][chapter][verse];
    }
    return null;
}

// Function to get a range of verses
function getVerses(book, chapter, startVerse, endVerse) {
    const verses = [];
    if (KJV_DATA[book] && KJV_DATA[book][chapter]) {
        for (let v = startVerse; v <= endVerse; v++) {
            if (KJV_DATA[book][chapter][v]) {
                verses.push({
                    verse: v,
                    text: KJV_DATA[book][chapter][v]
                });
            }
        }
    }
    return verses;
}

// Function to search verses
function searchVerses(searchTerm, books = ['Matthew', 'Mark', 'Luke', 'John']) {
    const results = [];
    const term = searchTerm.toLowerCase();
    
    books.forEach(book => {
        if (KJV_DATA[book]) {
            Object.keys(KJV_DATA[book]).forEach(chapter => {
                Object.keys(KJV_DATA[book][chapter]).forEach(verse => {
                    const text = KJV_DATA[book][chapter][verse];
                    if (text.toLowerCase().includes(term)) {
                        results.push({
                            book: book,
                            chapter: chapter,
                            verse: verse,
                            text: text
                        });
                    }
                });
            });
        }
    });
    
    return results;
}

